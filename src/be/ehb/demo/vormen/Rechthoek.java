package be.ehb.demo.vormen;

public class Rechthoek {

    int basis;
    int hoogte;

    public Rechthoek(int basis, int hoogte) {
        this.basis = basis;
        this.hoogte = hoogte;
    }

    public int getBasis() {
        return basis;
    }

    public void setBasis(int basis) {
        this.basis = basis;
    }

    public int getHoogte() {
        return hoogte;
    }

    public void setHoogte(int hoogte) {
        this.hoogte = hoogte;
    }


}
